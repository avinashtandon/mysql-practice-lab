-- ============================================================
-- 4. MIN()
-- ============================================================
--
-- Returns the smallest value in a column.
-- ============================================================


-- ============================================================
-- Example Query
-- ============================================================

SELECT MIN(revenue)
FROM events;

-- Result → 50


-- ============================================================
-- Real Data Engineering Use Case
-- ============================================================
--
-- Finding the earliest timestamp:
--
--   SELECT MIN(event_time)
--   FROM logs;
--
-- Used to find:
--   • First user activity
--   • First purchase date
--   • Session start time
-- ============================================================

-- ============================================================
-- Example Table: events
-- ============================================================
-- | user_id | event_type | revenue |
-- |---------|------------|---------|
-- | 1       | click      | NULL    |
-- | 1       | purchase   | 50      |
-- | 2       | click      | NULL    |
-- | 2       | purchase   | 100     |
-- ============================================================

-- ============================================================
-- Example Table: logs
-- ============================================================
-- | user_id | event_time          | action      |
-- |---------|---------------------|-------------|
-- | 1       | 2023-10-01 10:00:00 | login       |
-- | 1       | 2023-10-01 10:05:00 | view_item   |
-- | 2       | 2023-10-01 10:10:00 | login       |
-- | 1       | 2023-10-01 10:15:00 | add_to_cart |
-- ============================================================