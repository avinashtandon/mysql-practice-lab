-- ============================================================
-- 2. SUM()
-- ============================================================
--
-- Adds numeric values together.
-- ============================================================


-- ============================================================
-- Example Query
-- ============================================================

SELECT SUM(revenue)
FROM events;

-- Result → 150


-- ============================================================
-- Important Behavior
-- ============================================================
--
-- NULL values are ignored by the SUM function.
--
-- Example calculation from the events table:
-- NULL + 50 + NULL + 100 = 150
-- ============================================================


