-- ============================================================
-- Table: orders
-- | order_id | user_id | amount |
-- |----------|---------|--------|
-- | 1        | 101     | 200    |
-- | 2        | 101     | 150    |
-- | 3        | 102     | 300    |
-- | 4        | 103     | 250    |
-- | 5        | 103     | 100    |
-- | 6        | 103     | 50     |
--
-- Business Problem
-- Product analytics wants to find users who placed **more than 2 orders**.
--
-- Return:
-- | user_id | order_count |
-- ============================================================

-- Write your query below:
--
SELECT user_id,
       COUNT(*) AS order_count
FROM orders 
GROUP BY user_id
HAVING COUNT(*) > 2;
