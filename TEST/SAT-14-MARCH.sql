-- Question 1 — Daily Paying Users
-- ============================================================
-- Table: orders
-- | order_id | user_id | order_time  | amount |
-- |----------|---------|-------------|--------|
-- | 1        | 101     | 2026-03-10  | 200    |
-- | 2        | 102     | 2026-03-10  | 0      |
-- | 3        | 101     | 2026-03-10  | 100    |
-- | 4        | 103     | 2026-03-11  | 300    |
-- | 5        | 104     | 2026-03-11  | 0      |
--
-- Business Problem
-- Finance wants to measure the number of **paying users per day**.
-- A paying user is someone whose order amount is greater than 0.
--
-- Return:
-- | order_date | paying_users |
-- ============================================================

-- Write your query below:
--
SELECT order_time AS order_date,
       COUNT(DISTINCT user_id) AS paying_users
FROM orders
WHERE amount > 0
GROUP BY order_time;
--



